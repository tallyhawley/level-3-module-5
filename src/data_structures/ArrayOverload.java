package data_structures;

import java.util.ArrayList;

public class ArrayOverload {
		public static void main(String[] args) {
			ArrayList<Integer> array = new ArrayList<Integer>();
			boolean tru = true;
			int counter = 0;
			while(tru) {
				array.add(1934820);
				counter++;
				System.out.println(counter);
			}
		}
}
